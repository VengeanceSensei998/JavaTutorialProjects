    private void jbtnDigitEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDigitEqualActionPerformed
        NumEnter2 = Double.parseDouble(jtxtResult.getText());
        if ( Op == "/")
        {
            Result = NumEnter1 / NumEnter2;
        }
        else if (Op == "-")
        {
            Result = NumEnter1 - NumEnter2;
        }
        else if (Op == "+")
        {
            Result = NumEnter1 + NumEnter2;
        }
        else if(Op == "*")
        {
            Result = NumEnter1 * NumEnter2;
        }
        jtxtResult.setText(String.valueOf(Result));
    }//GEN-LAST:event_jbtnDigitEqualActionPerformed

